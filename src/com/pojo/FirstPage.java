package com.pojo;

public class FirstPage {
    private int warnNumber;
    private int dicNumber;
    private int tolNumber;
    private String mName;

    public int getWarnNumber() {
        return warnNumber;
    }

    public void setWarnNumber(int warnNumber) {
        this.warnNumber = warnNumber;
    }

    public int getDicNumber() {
        return dicNumber;
    }

    public void setDicNumber(int dicNumber) {
        this.dicNumber = dicNumber;
    }

    public int getTolNumber() {
        return tolNumber;
    }

    public void setTolNumber(int tolNumber) {
        this.tolNumber = tolNumber;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }
}
